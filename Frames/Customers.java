    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Customer c;
        String name = nameField.getText();
        String ID = IDField.getText();
        String email = emailField.getText();;
        String address = addressField.getText();
        if (name == null || name.equals("") || email == null || email.equals("") || address == null || address.equals("") || ID == null || ID.equals("")) {
            filllabel.setVisible(true);
        } else {
            c = new Customer(name, ID, email, address, 0);
            updateTable(c);

            nameField.setText("");
            emailField.setText("");
            addressField.setText("");
            IDField.setText("");

            nameField.setVisible(false);
            emailField.setVisible(false);
            addressField.setVisible(false);
            IDField.setVisible(false);
            saveButton.setVisible(false);
            namelabel.setVisible(false);
            adresslabel.setVisible(false);
            emaillabel.setVisible(false);
            idlabel.setVisible(false);

            deletelabel.setVisible(false);
            openlabel.setVisible(false);

            filllabel.setVisible(false);
        }

    }//GEN-LAST:event_saveButtonActionPerformed

