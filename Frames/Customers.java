    private void saveButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButton1ActionPerformed

        Customer c = null;

        String name;
        String email;
        String address;
        String id;
        int numberOfBooks;
        Library.deleteAllCustomers();

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            name = (String) jTable1.getValueAt(i, 0);
            id = (String) jTable1.getValueAt(i, 1);
            email = (String) jTable1.getValueAt(i, 2);
            address = (String) jTable1.getValueAt(i, 3);
            numberOfBooks = (int) jTable1.getValueAt(i, 4);

            c = new Customer(name, id, email, address, numberOfBooks);

            Library.addCustomer(c);

        }
        if (c != null) {
            updateTable();
        }

    }//GEN-LAST:event_saveButton1ActionPerformed

